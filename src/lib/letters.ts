// 위기 키워드 감지 정규식 (자살, 자해, 죽고 싶다 등) — FR-5.5, 무변경
const CRISIS_KEYWORDS = /(자살|자해|죽고 싶|죽어야|죽음|살기 싫|극단적 선택|생을 마감)/i;

const CRISIS_MESSAGE = `

---
⚠️ [안내] 당신의 마음에 깊은 밤이 찾아왔을 때, 혼자 고민하지 마세요. 
언제든 따뜻하게 당신의 이야기를 들어줄 곳이 있습니다.
- 자살예방 상담전화: ☎️ 109 (24시간 무료 운영)
- 정신건강 상담전화: ☎️ 1577-0199`;

function pickRandom<T>(items: T[]): T {
  return items[Math.floor(Math.random() * items.length)];
}

function scoreTopics(normalized: string): Map<Exclude<TopicSlug, 'generic'>, number> {
  const scores = new Map<Exclude<TopicSlug, 'generic'>, number>();
  for (const topic of MATCH_TOPICS) {
    let score = 0;
    for (const keyword of TOPIC_KEYWORDS[topic]) {
      // 키워드당 최대 1 (부분 문자열 포함, 대소문자 무시된 normalize 기준)
      if (normalized.includes(keyword.toLowerCase())) {
        score += 1;
      }
    }
    scores.set(topic, score);
  }
  return scores;
}

function selectLetter(confessionText: string, tone: 'angel' | 'devil'): LetterTemplate {
  const normalized = confessionText.toLowerCase();
  const scores = scoreTopics(normalized);

  let max = 0;
  for (const score of scores.values()) {
    if (score > max) max = score;
  }

  const best =
    max > 0
      ? MATCH_TOPICS.filter((topic) => scores.get(topic) === max)
      : [];

  let chosenTopic: TopicSlug = 'generic';
  if (best.length > 0) {
    chosenTopic = pickRandom(best);
  }

  let pool = LETTERS.filter((letter) => letter.tone === tone && letter.topic === chosenTopic);
  // 주제 풀이 비어 있으면(구현 오류) generic 폴백
  if (pool.length === 0) {
    pool = LETTERS.filter((letter) => letter.tone === tone && letter.topic === 'generic');
  }

  return pickRandom(pool);
}

/** 고해 본문 + 톤 → 편지 본문. 위기 키워드는 선택 이후·반환 직전 부착. */
export function generateLetterContent(userContent: string, tone: 'angel' | 'devil'): string {
  let content = selectLetter(userContent, tone).content;

  if (CRISIS_KEYWORDS.test(userContent)) {
    content += CRISIS_MESSAGE;
  }

  return content;
}
